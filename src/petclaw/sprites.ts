export interface SpriteData {
  palette: Palette;
  pixels: number[][];
  size: number;
}

export function getSprite(action: PetAction, frame: number): SpriteData {
  const frames = ADULT_SPRITES.frames[action];
  const index = frame % frames.length;

  return {
    pixels: frames[index],
    palette: ADULT_SPRITES.palette,
    size: ADULT_SPRITES.size,
  };
}

export function getFrameCount(action: PetAction): number {
  return ADULT_SPRITES.frames[action].length;
}
